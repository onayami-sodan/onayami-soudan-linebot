/**
 * 恋愛診断の各設問の出し方（既存コード互換）
 * @param {object} q - { text, choices[] } 形式
 * @param {number} index1 - 1始まりの設問番号
 * @param {number} total  - 総設問数
 */
export function HOW_TO_ANSWER(q, index1, total) {
  const list = (q?.choices || []).map((c, i) => `${i + 1}. ${c}`).join('\n')
  return [
    `Q${index1}/${total}`,
    `${q?.text || ''}`,
    '',
    list,
    '',
    '番号（半角）で答えてね（例：1）'
  ].join('\n')
}
